const month = 3
switch (month) {
    case 1:
        console.log("January",);
        break;
        case 2:
            console.log("Febuary",);
            case 3:
                console.log("March",);
                case 4:
                    console.log("April",);
                    case 5:
                        console.log("May",);
                        case 6:
                            console.log("june",);
    default:
        console.log("Default case matched",);
        break;
}
